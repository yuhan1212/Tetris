    public Color[][] getColors() {
        Color[][] colors = new Color[20][10];
        for (int r = 0; r < 20; r++) {
            for (int c = 0; c < 10; c++) {
                colors[r][c] = Color.GREEN;
            }
        }
        return colors;
    }

